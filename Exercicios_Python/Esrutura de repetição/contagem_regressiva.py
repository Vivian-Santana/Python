'''
Neste exemplo, inicializei a variável num com o valor 10. Em seguida, 
usei um loop while para repetir o bloco de código, enquanto num for maior 
ou igual a 0. Dentro do loop, imprime o valor atual de num e, em seguida, 
decrementei num em 1 usando o operador -=. 
Isso faz com que num diminua a cada iteração do loop.
A saída será uma contagem regressiva de 10 a 0:
Dessa forma, o loop while executará até que num seja menor que 0, 
imprimindo cada valor em ordem decrescente.
'''

print('contagem regressiva para o lançamento 🧑‍🚀👩‍🚀')

x = 10
while x >= 0:
    print(x)
    x -= 1
print('Fogoooooooooooooo! 🚀')